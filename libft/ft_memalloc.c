#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void *temp;
	char *str;

	if (!(temp = malloc(size)))
		return (NULL);
	str = (char*)temp;
	while (size--)
		str[size] = 0;
	return (temp);
}
